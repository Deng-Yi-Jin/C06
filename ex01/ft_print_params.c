#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	c;

	i = 1;
	while (i < argc)
	{
		c = 0;
		while (argv[i][c])
		{
			write(1, &argv[i][c], 1);
			c++;
		}
		i++;
		write(1, "\n", 1);
	}
}
